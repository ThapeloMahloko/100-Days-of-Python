"""
Word Replacement: Replace all occurrences of a word in a given text with a new word.
"""
# Define the sentance
sentence = "Hello Thapelo, how are you doing today?"
# Define the word to be replaced
word_to_replace = "Thapelo"
# Define the new word
new_word = "Lebohang"
# Replace the word in the sentence
sentence = sentence.replace(word_to_replace, new_word)
# Print the modified sentence
print(sentence)
