"""
String Slicing: Print first 5, last 5 characters and slice from 3 to 8 index of the quote
"""
# Define quote
quote = "The quick brown fox jumps over the lazy dog"

# Print first 5 characters
print(quote[:5])

# Print last 5 characters
print(quote[-5:])

# Print slice from 3 to 8 index
print(quote[3:8])