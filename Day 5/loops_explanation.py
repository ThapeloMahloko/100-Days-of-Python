"""
Loops in Python

Loops are used to execute a block of code repeatedly. Python has two main types of loops: for loops and while loops.

1. For Loops:
For loops are used to iterate over a sequence (like a list, tuple, dictionary, set, or string) or other iterable objects.

Example of a for loop:
"""
# Example of a for loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

"""
This loop will print each fruit in the list.

2. While Loops:
While loops execute a block of code as long as a specified condition is true.

Example of a while loop:
"""
# Example of a while loop
count = 0
while count < 5:
    print(count)
    count += 1

"""
This loop will print numbers from 0 to 4.

Conclusion:
Loops are a powerful tool in Python that allow you to automate repetitive tasks. Understanding how to use loops effectively can greatly enhance your programming skills.
"""
