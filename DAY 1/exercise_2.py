"""
Basic Maths with Variables

1. Create two variables x and y with values
"""
x = 2
y = 13

"""
2. Perform the following operations and print the results:
Addition
Substraction
Multiplication
Division
Modulus (remainder)
"""
# Addition
print(f"Addition : {x} + {y} = {x + y}")

# Subtraction
print(f"Subtraction : {x} - {y} = {x - y}")

# Multiplication
print(f"Multiplication : {x} * {y} = {x * y}")

# Division
print(f"Division : {x} / {y} = {x / y}")

# Modulus (remainder)
print(f"Modulus (remainder) : {y} % {x} = {y % x}")