"""
Exploring Data types

1. Assign a value of each data type to a variable.
"""
# Integer
integer_var = 10

# Float
float_var = 10.5

# String
string_var = "Hello, Tlhali!"

# Boolean
boolean_var = True

# None
none_var = None

"""
2. Print the variable along with its data type
"""

# Integer
print(integer_var, type(integer_var))  
# Output: 10 <class 'int'

# Float
print(float_var, type(float_var))
# Output: 10.5 <class 'float'>

# String
print(string_var, type(string_var))
# Output: Hello, Tlhali! <class 'str'>

# Boolean
print(boolean_var, type(boolean_var))
# Output: True <class 'bool'>

# None
print(none_var, type(none_var))
# Output: None <class 'NoneType'>