# Addition 
print(51 + 5)

# Subtraction 
print(51 - 5)

# Multiplication
print(51 * 5)

# Division
print(51 / 5)

# Integer division
print(51 // 5)

# Exponential 
print(51 ** 5)

# BEDMAS is considered when having multiple operations
# 1. Brackets 
print((51 + 5) * 5)

# 2. Exponents
print(2 ** 3 + 5)

# Division or Multiplication
print(2 * 3 + 4 / 2)  # 6 + 2

# Addition or Subtraction
print(2 + 3 - 4)  # 1