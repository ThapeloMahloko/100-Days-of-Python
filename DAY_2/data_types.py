#1. Integers are whole numbers, either positive, negative, or zero.
# For example:
#   1, -1, 0, 123, -456, etc.
print(152)
#2. Floats are decimal numbers. For example:
#   3.14, -0.5, 0.123, etc.
print(3.14)
#3. Strings are sequences of characters. For example:
#   "hello", 'hello', "hello world", 'hello world', etc.
print("hello")
#4. Boolean values are either True or False. For example:
#   True, False, etc.
print(True)
# Print all data types

print(type("hello"))    # String
print(type(123))        # Integers
print(type(3.14))       # Floats
print(type(True))       # Boolean





