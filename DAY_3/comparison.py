# Comparison Operators
# 1. Equal to: a == b
# For example:
a = 5
b = 5
print(a == b)  # Output: True

# 2. Not Equal to: a != b
# For example:
a = 5
b = 10
print(a != b)  # Output: True

# 3. Greater than: a > b
# For example:
a = 10
b = 5
print(a > b)  # Output: True

# 4. Less than: a < b
# For example:
a = 5
b = 10
print(a < b)  # Output: True

# 5. Greater than or equal to: a >= b
# For example:
a = 10
b = 10
print(a >= b)  # Output: True
# 6. Less than or equal to: a <= b
# For example:
a = 5
b = 10
print(a <= b)  # Output: True




