def find_max(numbers):
    """
    Find the maximum number in a list.

    Parameters:
    numbers (list): A list of numbers.

    Returns:
    int/float: The maximum number in the list.
    """
    return max(numbers)

# Test the function
if __name__ == "__main__":
    print(find_max([1, 2, 3, 4, 5]))  # 5
